// IconComposerFormat
//
// A library for reading and writing Apple .icon bundles used by
// Xcode's Icon Composer app.
//
// Public API:
// - IconBundle: Read/write .icon bundle directories
// - IconDocument: The icon.json structure
// - IconGroup: Layer groups with rendering properties
// - IconLayer: Individual layers referencing assets
// - IconFill: Background fills (solid, gradient)
// - IconColor: Color values in various color spaces
// - IconPosition: Scale and translation
// - IconShadow: Shadow configuration
// - IconTranslucency: visionOS translucency
// - IconBlendMode: Layer blend modes
// - SupportedPlatforms: Platform targeting
// - Specialization: Appearance/idiom variants
